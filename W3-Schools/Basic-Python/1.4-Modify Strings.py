#15 Python - Modify Strings

'''
* Upper Case, print the string in upper case all

ex:
a = "Hello, World!"
print(a.upper())

* Lower Case, print the string in lower case all

ex:
a = "Hello, World!"
print(a.lower())

* Remove Whitespace, to remove any whitespace from the beginning or the end

ex: 
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

* Replace String, To replace a character or phrase in a string with a new one

ex: 
a = "Hello, World!"
print(a.replace("H", "J"))

* Split String, Split the string into sub-strings

ex: 
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
'''
